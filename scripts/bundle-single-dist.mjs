import { promises as fs } from 'node:fs';
import path from 'node:path';

import { build } from 'esbuild';

const rootDir = process.cwd();
const browserDir = path.join(rootDir, 'dist', 'dynamic-site', 'browser');
const indexPath = path.join(browserDir, 'index.html');
const tempEntryPath = path.join(browserDir, '__single-entry__.mjs');
const outputBundleName = 'app-single-esbuild.js';
const outputBundlePath = path.join(browserDir, outputBundleName);
const shouldUpdateIndex = process.argv.includes('--update-index');

const findSingleFileByPrefix = async (prefix) => {
  const entries = await fs.readdir(browserDir);
  const candidates = entries.filter(
    (name) => name.startsWith(prefix) && name.endsWith('.js') && !name.endsWith('.js.gz') && !name.endsWith('.js.br')
  );

  if (candidates.length !== 1) {
    throw new Error(`Expected exactly one ${prefix}*.js file in dist, found ${candidates.length}.`);
  }

  return candidates[0];
};

const updateIndexHtml = async () => {
  const raw = await fs.readFile(indexPath, 'utf8');

  // Remove modulepreload links generated by Angular for chunk graph.
  let next = raw.replace(/\s*<link\s+rel="modulepreload"\s+href="[^"]+"\s*\/>\s*/g, '\n');

  // Remove existing candidate script tags from previous experiments.
  next = next
    .replace(/\s*<script\s+src="polyfills-[^"]+\.js"\s+type="module"\s*><\/script>\s*/g, '\n')
    .replace(/\s*<script\s+src="main-[^"]+\.js"\s+type="module"\s*><\/script>\s*/g, '\n')
    .replace(/\s*<script\s+src="pepo\.js"\s+type="module"\s*><\/script>\s*/g, '\n')
    .replace(/\s*<script\s+src="app-combined\.js"\s+type="module"\s*><\/script>\s*/g, '\n')
    .replace(/\s*<script\s+src="app-single-esbuild\.js"\s+type="module"\s*><\/script>\s*/g, '\n');

  next = next.replace(/\s*<\/body>/, `\n    <script src="${outputBundleName}" type="module"></script>\n  </body>`);

  await fs.writeFile(indexPath, next, 'utf8');
};

const run = async () => {
  const polyfillsFile = await findSingleFileByPrefix('polyfills-');
  const mainFile = await findSingleFileByPrefix('main-');

  const entrySource = `import './${polyfillsFile}';\nimport './${mainFile}';\n`;
  await fs.writeFile(tempEntryPath, entrySource, 'utf8');

  try {
    await build({
      entryPoints: [tempEntryPath],
      outfile: outputBundlePath,
      bundle: true,
      format: 'esm',
      platform: 'browser',
      target: ['es2022'],
      minify: true,
      sourcemap: false,
      legalComments: 'none',
      logLevel: 'info',
    });
  } finally {
    await fs.rm(tempEntryPath, { force: true });
  }

  if (shouldUpdateIndex) {
    await updateIndexHtml();
  }

  const stat = await fs.stat(outputBundlePath);
  console.log(`Created ${outputBundleName} (${Math.round(stat.size / 1024)} KiB)`);
  if (shouldUpdateIndex) {
    console.log('Updated dist/dynamic-site/browser/index.html to use single script bundle.');
  }
};

run().catch((error) => {
  console.error('[bundle-single-dist] Failed:', error.message);
  process.exitCode = 1;
});
